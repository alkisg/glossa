Δεδομένα = {
    όνομα: inputText("Όνομα:"),
    ηλικία: inputNumber("Ηλικία:")
}

function Αλγόριθμος(Δεδομένα, Αποτελέσματα) {
    var όνομα = Δεδομένα.όνομα;
    var ηλικία = Δεδομένα.ηλικία;
    var μήνυμα = "";

    if (ηλικία < 18) {
        μήνυμα = `Ο/Η ${όνομα} είναι ανήλικος`;
    } else {
        μήνυμα = `Ο/Η ${όνομα} είναι ενήλικας`;
    }

    Αποτελέσματα.μήνυμα = μήνυμα;
}
